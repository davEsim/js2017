            <p>People in Need provides immediate humanitarian assistance to people in trouble during&nbsp;<strong>natural disasters or in conflict crises</strong>, both in the Czech Republic (e.g. during floods) and abroad. At the same time, it focuses on arranging long-term assistance for people existing in poverty and poor living conditions (Africa, Asia, and Europe).</p>

